                <h2 class="page-header-dashed">Inline text elements</h2>
                <h3>Marked text</h3>
                <p>For highlighting a run of text due to its relevance in another context, use the <code>&lt;mark&gt;</code> tag.</p>
                <div class="bs-example">
                    You can use the mark tag to <mark>highlight</mark> text.
                </div>
                <div class="highlight">
<pre><code>You can use the mark tag to &lt;mark&gt;highlight&lt;/mark&gt; text.</code></pre>
                </div>
                <h3>Deleted text</h3>
                <p>For indicating blocks of text that have been deleted use the <code>&lt;del&gt;</code> tag.</p>
                <div class="bs-example">
                    <p><del>This line of text is meant to be treated as deleted text.</del></p>
                </div>
                <div class="highlight">
<pre><code>&lt;del&gt;This line of text is meant to be treated as deleted text.&lt;/del&gt;</code></pre>
                </div>
                <h3>Strikethrough text</h3>
                <p>For indicating blocks of text that are no longer relevant use the <code>&lt;s&gt;</code> tag.</p>
                <div class="bs-example">
                    <p><s>This line of text is meant to be treated as no longer accurate.</s></p>
                </div>
                <div class="highlight">
<pre><code>&lt;s&gt;This line of text is meant to be treated as no longer accurate.&lt;/s&gt;</code></pre>
                </div>
                <h3>Inserted text</h3>
                <p>For indicating additions to the document use the <code>&lt;ins&gt;</code> tag.</p>
                <div class="bs-example">
                    <p><ins>This line of text is meant to be treated as an addition to the document.</ins></p>
                </div>
                <div class="highlight">
<pre><code>&lt;ins&gt;This line of text is meant to be treated as an addition to the document.&lt;/ins&gt;</code></pre>
                </div>
                <h3>Underlined text</h3>
                <p>To underline text use the <code>&lt;u&gt;</code> tag.</p>
                <div class="bs-example">
                    <p><u>This line of text will render as underlined</u></p>
                </div>
                <div class="highlight">
<pre><code>&lt;u&gt;This line of text will render as underlined&lt;/u&gt;</code></pre>
                </div>
                <h3>Small text</h3>
                <p>For de-emphasizing inline or blocks of text, use the <code>&lt;small&gt;</code> tag to set text at 85% the size of the parent. Heading elements receive their own <code>font-size</code> for nested <code>&lt;small&gt;</code> elements.</p>
                <p>You may alternatively use an inline element with <code>.small</code> in place of any <code>&lt;small&gt;</code>.</p>
                <div class="bs-example">
                    <p><small>This line of text is meant to be treated as fine print.</small></p>
                </div>
                <div class="highlight">
<pre><code>&lt;small&gt;This line of text is meant to be treated as fine print.&lt;/small&gt;</code></pre>
                </div>
                <h3>Bold</h3>
                <p>For emphasizing a snippet of text with a heavier font-weight.</p>
                <div class="bs-example">
                    <p>The following snippet of text is <strong>rendered as bold text</strong>.</p>
                </div>
                <div class="highlight">
<pre><code>&lt;strong&gt;rendered as bold text&lt;/strong&gt;</code></pre>
                </div>
                <h3>Italics</h3>
                <p>For emphasizing a snippet of text with italics.</p>
                <div class="bs-example">
                    <p>The following snippet of text is <em>rendered as italicized text</em>.</p>
                </div>
                <div class="highlight">
<pre><code>&lt;em&gt;rendered as italicized text&lt;/em&gt;</code></pre>
