        <h1 class="page-header-dashed">Lobibox</h1>
        <p class="lead">Lobibox is responsive jquery notification plugin specially written for <big>LobiAdmin</big>.</p>

        <p>Lobibox is devided into two parts</p>
        <ul>
            <li>Messageboxes</li>
            <li>Notifications</li>
        </ul>

        <h3>Features</h3>
        <div class="bs-example">
            <h4>Messageboxes</h4>
            <ul>
                <li>Lobibox messages boxes can be modal and not modal as well.</li>
                <li>You can show multiple messages at the same time.</li>
                <li>Every message can be draggable (<span class="text-warning">On small screens dragging is disabled</span>)</li>
                <li>
                    You can show
                    <ul>
                        <li><b>messages in different colors and icons</b></li>
                        <li><b>confirm message</b></li>
                        <li><b>one line prompt</b></li>
                        <li><b>multiline prompt</b></li>
                        <li><b>Any HTML5 input type can be used in prompt window. Such as: text, color, date, datetime, email, number range ...</b></li>
                        <li><b>Progress messagebox</b>. Lobibox comes with default progress style but you can use bootstrap style progress bar</li>
                        <li><b>custom content window with ajax support with custom action buttons</b></li>
                    </ul>
                </li>
                <li>Every message is an instance of "class". You can easily grab the instance and attach events or call methods directly by the instance. <small>See examples bellow.</small> </li>
            </ul>
            <h4>Notifications</h4>
            <ul>
                <li>Different color support</li>
                <li>Possibility to show in any corners of the screen</li>
                <li>Delay after which it disappears</li>
                <li>Show delay indicator</li>
                <li>Show with image</li>
                <li>Sound support</li>
                <li>Size support. You can show notifications of different size</li>
            </ul>
        </div>
    </div>
