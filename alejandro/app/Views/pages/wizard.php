            'assets/js/plugin/jquery-validation/additional-methods.min.js',
            'assets/js/plugin/jasny-bootstrap/jasny-bootstrap.min.js'
        ], initPage);
        
        function initPage(){
            initValidationDefaults();
            
            var masks = $('.inputmask');
            masks.each(function(ind, el){
               var $el = $(el);
               var obj= {};
               if ($el.data('mask')){
                   obj.mask = $el.data('mask');
               }
               if ($el.data('mask-placeholder')){
                   obj.placeholder = $el.data('mask-placeholder');
               }
               $el.inputmask(obj);
            });
            
            $('#demo-form1, #demo-form2').each(function(index, form){
                $(this).validate({
                    rules: {
                        email: {
                            required: true,
                            email: true
                        },
                        firstname: {
                            required: true
                        },
                        lastname: {
                            required: true
                        },
                        phone: {
                            required: true
                        },
                        company: {
                            required: true
                        },
                        address1: {
                            required: true
                        },
                        city: {
                            required: true
                        },
                        state: {
                            required: true
                        },
                        zipcode: {
                            required: true
                        },
                        country: {
                            required: true
                        },
                        'ship_info': {
                            required: true
                        },
                        credit_card: {
                            required: true
                        },
                        exp_month: {
                            required: true
                        },
                        exp_year: {
                            required: true
                        },
                        cvv: {
                            required: true
                        }
                    }
                });
            });
            $('#demo-wizard1').bootstrapWizard({
                onTabClick: function(li, ul, ind, ind2){
                    var $newli = ul.find('li').eq(ind2);
                    if ($newli.hasClass('complete')){
                        return true;
                    }else{
                        return false;
                    }
                },
                onNext: function(li, ul, index){
                    var $wizard = ul.closest('.wizard');
                    var $pane = $wizard.find('.tab-pane').eq(index - 1);
                    var $fields = $pane.find('[name]');
                    var $form = $('#demo-form1');
                    var validator = $form.validate();
                    validator.form();
                    var ISVALID = true;
                    $fields.each(function(index, field){
                        var fieldSelector = '#demo-form1 [name="'+field.name+'"]';
                        validator.element(fieldSelector);
                        if ( ! validator.valid(fieldSelector)){
                            ISVALID = false;
                        }
                    });
                    if (ISVALID){
                        li.addClass('complete');
                    }
                    return ISVALID;
                }
            });
            $('#demo-wizard2').bootstrapWizard({
                onTabClick: function(li, ul, ind, ind2){
