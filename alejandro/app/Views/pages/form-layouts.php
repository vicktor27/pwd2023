        function initPage(){
            $('.panel').lobiPanel({
                editTitle: false,
                sortable: true,
                unpin: false,
                reload: false
            });
            var masks = $('.inputmask');
            masks.each(function(ind, el){
               var $el = $(el);
               var obj= {};
               if ($el.data('mask')){
                   obj.mask = $el.data('mask');
               }
               if ($el.data('mask-placeholder')){
                   obj.placeholder = $el.data('mask-placeholder');
               }
               $el.inputmask(obj);
            });
        }
    </script>
