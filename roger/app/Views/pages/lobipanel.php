<div id="lobipanel-documentation">
    <h1 class="page-header-dashed">LobiPanel</h1>
    <p class="lead">
        LobiPanel is jquery plugin for bootstrap panels specially created for <big>LobiAdmin</big>. 
        It extends panels with several common and useful functions which does not come by default.
    </p>
    <div id="lobipanel-usage">
        <h3>Basic usage</h3>
        <p>LobiPanel initialization is simple. Just create correct markup of bootstrap panel and call plugin</p>
        <div class="highlight">
            <pre>
<code class="html">&lt;div class=&quot;panel panel-default&quot;&gt;
    &lt;div class=&quot;panel-heading&quot;&gt;
        &lt;div class=&quot;panel-title&quot;&gt;
            &lt;h4&gt;Panel title&lt;/h4&gt;
        &lt;/div&gt;
    &lt;/div&gt;
    &lt;div class=&quot;panel-body&quot;&gt;
        Lorem ipsum...
    &lt;/div&gt;
&lt;/div&gt;
&lt;script&gt;</code>
<code class="javascript">   $(function(){
        $('.panel').lobiPanel({
            //Options go here
            ...
        });
    });</code>
<code class="html">&lt;/script&gt;</code>
            </pre>
        </div>
        <p>And you will get the following result. </p>
        <div class="panel panel-light" id="demoPanel11">
            <div class="panel-heading">
                <div class="panel-title">
                    <h4>Panel title</h4>
                </div>
            </div>
            <div class="panel-body">
                Lorem ipsum...
            </div>
        </div>
        <script type="text/javascript">
            $('#demoPanel11').lobiPanel();
        </script>
        <h4>Using buttons on the right side of the panel heading you can perform several actions.</h4>
        <ul>
            <li>
                This button <span class="glyphicon glyphicon-remove"></span>  removes the panel from document. 
                <p><span class="text-warning-dark">Warning!!!. This operation can not be undone.</span></p>
            </li>
            <li>This button <span class="glyphicon glyphicon-resize-full"></span> expands the panel on full screen.</li>
            <li>This button <span class="glyphicon glyphicon-refresh"></span> loads content in <code>.panel-body</code> from specified URL.</li>
            <li>
                This button <span class="glyphicon glyphicon-move"></span> takes out the panel from its position in DOM hierarchy and makes it floating.
                <p>Anytime you like (with the same button) you can put the panel in its position.</p>
            </li>
            <li>
                This button <span class="glyphicon glyphicon-minus"></span> has two functions.
                <ol>
                    <li>It collapses and expands the panel.</li>
                    <li>It minimizes the panel at the bottom of the page when it is <b>taken out from DOM hierarchy</b>.</li>
                </ol>
            </li>
        </ul>
